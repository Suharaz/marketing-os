interface PlatformIconProps {
  platform: string;
  className?: string;
  // When true: render a colored circular badge (white icon on brand color).
  // When false: render a plain icon using currentColor.
  badge?: boolean;
  size?: number;
}

// Normalize free-text platform values from DB to our known keys.
function resolve(platform: string): Platform | null {
  const p = platform.toLowerCase().trim();
  if (p in PLATFORM_META) return p as Platform;
  if (p === 'fb') return 'facebook';
  if (p === 'ig') return 'instagram';
  if (p === 'yt') return 'youtube';
  if (p === 'tt') return 'tiktok';
  if (p === 'th') return 'threads';
  if (p === 'zl' || p === 'zalo-oa') return 'zalo';
  if (p === 'x' || p === 'tw') return 'twitter';
  if (p === 'in' || p === 'li') return 'linkedin';
  return null;
}

export function PlatformIcon({
  platform,
  className,
  badge = false,
  size = 20,
}: PlatformIconProps) {
  const key = resolve(platform);

  // Unknown platform → neutral fallback square with first letter.
  if (!key) {
    return (
      <span
        className={cn(
          'inline-flex items-center justify-center rounded-md bg-zinc-200 text-[10px] font-bold text-zinc-600',
          className
        )}
        style={{ width: size, height: size }}
        aria-label={platform}
      >
        {platform.slice(0, 1).toUpperCase()}
      </span>
    );
  }

  const meta = PLATFORM_META[key];
  const svg = (
    <svg
      viewBox="0 0 24 24"
      fill="currentColor"
      width={size}
      height={size}
      aria-label={key}
      role="img"
    >
      <path d={meta.path} />
    </svg>
  );

  if (badge) {
    return (
      <span
        className={cn(
          'inline-flex items-center justify-center rounded-full text-white shrink-0',
          meta.bg,
          className
        )}
        style={{ width: size + 12, height: size + 12 }}
      >
        <svg
          viewBox="0 0 24 24"
          fill="currentColor"
          width={size * 0.65}
          height={size * 0.65}
          aria-label={key}
          role="img"
        >
          <path d={meta.path} />
        </svg>
      </span>
    );
  }

  return (
    <span
      className={cn('inline-flex shrink-0', className)}
      style={{ color: meta.color }}
    >
      {svg}
    </span>
  );
}
